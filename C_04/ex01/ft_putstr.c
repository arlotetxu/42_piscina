#include<unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main ()
{
	char	texto[] = "1234567890";

	ft_putstr(texto);
	return (0);
}
